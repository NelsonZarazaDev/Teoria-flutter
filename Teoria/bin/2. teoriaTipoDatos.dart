void main() {
  //Tipo texto
  String nombre = "Nelson";
  print(nombre);

  //Tipo numerico
  int anios = 2003;
  print(anios);

  //Tipo decimal
  double precio = 40.000;
  print(precio);

  //Tipo booleano
  bool verdadero = true;
  bool falso = false;
  print(verdadero);
  print(falso);

  // Tipo dnamico
  dynamic numLet = 1;
  print(numLet);
  numLet = "uno";
  print(numLet); 
}
